{
	HAL_CAN_GetRxMessage(hcan, CAN_RX_FIFO0, &RxHeader, RxData);
	if (RxHeader.StdId == 0x625) {
	    // Handle data for the expected message ID (0x100)
	    // Example: Print received data to serial console
	    printf("Received message with ID 0x625 and data: ");
	    for (int i = 0; i < RxHeader.DLC; ++i) {
	      printf("%02X ", RxData[i]);
	    }
	    printf("\n");
	}
	count++;
}

